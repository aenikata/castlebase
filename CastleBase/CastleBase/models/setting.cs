
using System;
using Castle.ActiveRecord;

namespace CastleBase
{

	[ActiveRecord]
	public class setting : ActiveRecordBase<setting>
	{

		public setting ()
		{
		}
		
		[PrimaryKey]
		public int Id {get;set;}
		[Property]
		public string Name {get;set;}
		[Property]
		public string Value {get;set;}
	}
}
